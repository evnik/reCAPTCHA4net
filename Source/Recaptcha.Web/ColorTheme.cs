using System;

namespace Recaptcha.Web
{
    /// <summary>
    /// Represents the color theme of the reCAPTCHA widget as defined at https://developers.google.com/recaptcha/docs/display#config.
    /// </summary>
    public enum ColorTheme
    {
        /// <summary>
        /// The widget is dark.
        /// </summary>
        Dark = 1,

        /// <summary>
        /// The widget is light.
        /// </summary>
        Light = 2
    }
}
